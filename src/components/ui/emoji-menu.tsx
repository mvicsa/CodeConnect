export const EmojiMenu: React.FC<EmojiMenuProps> = ({
  onEmojiSelect,
  trigger,
  className,
  position = 'top',
  align = 'center'
}) => {
  const [isOpen, setIsOpen] = useState(false);
  const [searchQuery, setSearchQuery] = useState('');
  const [selectedCategory, setSelectedCategory] = useState(0);
  const searchInputRef = useRef<HTMLInputElement>(null);

  // Filter emojis based on search query
  const filteredEmojis = emojiCategories.map(category => ({
    ...category,
    emojis: category.emojis.filter(emoji => 
      emoji.emoji.includes(searchQuery) || 
      emoji.keywords.some(keyword => keyword.includes(searchQuery.toLowerCase())) || 
      category.name.toLowerCase().includes(searchQuery.toLowerCase())
    )
  })).filter(category => category.emojis.length > 0);

  // Handle emoji selection
  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
    setIsOpen(false);
    setSearchQuery('');
  };

  // Handle keyboard navigation
  useEffect(() => {
    if (isOpen && searchInputRef.current) {
      searchInputRef.current.focus();
    }
  }, [isOpen]);

  // Handle escape key to close
  useEffect(() => {
    const handleEscape = (e: KeyboardEvent) => {
      if (e.key === 'Escape' && isOpen) {
        setIsOpen(false);
      }
    };

    document.addEventListener('keydown', handleEscape);
    return () => document.removeEventListener('keydown', handleEscape);
  }, [isOpen]);

  // Handle global keyboard shortcut to open
  useEffect(() => {
    const handleKeydown = (e: KeyboardEvent) => {
      if (e.key === ':' && e.ctrlKey) {
        e.preventDefault();
        setIsOpen(true);
      }
    };

    document.addEventListener('keydown', handleKeydown);
    return () => document.removeEventListener('keydown', handleKeydown);
  }, []);

  return (
    <Popover open={isOpen} onOpenChange={setIsOpen}>
      <PopoverTrigger asChild>
        {trigger || (
          <Button
            variant="ghost"
            size="icon"
            className={cn("h-8 w-8", className)}
          >
            <Smile className="h-4 w-4" />
          </Button>
        )}
      </PopoverTrigger>
      <PopoverContent 
        className="bg-background w-80 p-0" 
        align={align}
        side={position}
      >
        <div className="p-3 border-b">
          <div className="relative">
            <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 h-4 w-4 text-muted-foreground" />
            <Input
              ref={searchInputRef}
              placeholder="Search emojis..."
              value={searchQuery}
              onChange={(e) => setSearchQuery(e.target.value)}
              className="pl-9"
            />
          </div>
        </div>

        {/* Category tabs */}
        {!searchQuery && (
          <div className="flex border-b overflow-x-auto">
            {emojiCategories.map((category, index) => (
              <button
                key={category.name}
                onClick={() => setSelectedCategory(index)}
                className={cn(
                  "flex-shrink-0 px-3 py-2 text-sm font-medium border-b-2 transition-colors cursor-pointer",
                  selectedCategory === index
                    ? "border-primary text-primary"
                    : "border-transparent text-muted-foreground hover:text-foreground"
                )}
              >
                <span>{category.icon}</span>
              </button>
            ))}
          </div>
        )}

        {/* Emoji grid */}
        <ScrollArea className="h-64">
          <div className="p-3">
            {searchQuery ? (
              // Search results
              <div className="grid grid-cols-8 gap-2">
                {filteredEmojis.flatMap(category => 
                  category.emojis.map((emoji, index) => (
                    <button
                      key={`${category.name}-${index}`}
                      onClick={() => handleEmojiClick(emoji.emoji)}
                      className="w-8 h-8 text-lg hover:bg-accent rounded transition-colors flex items-center justify-center"
                      title={emoji.emoji}
                    >
                      {emoji.emoji}
                    </button>
                  ))
                )}
              </div>
            ) : (
              // Category view
              <div className="grid grid-cols-8 gap-2">
                {emojiCategories[selectedCategory]?.emojis.map((emoji, index) => (
                  <button
                    key={index}
                    onClick={() => handleEmojiClick(emoji.emoji)}
                    className="w-8 h-8 text-lg hover:bg-accent rounded transition-colors flex items-center justify-center"
                    title={emoji.emoji}
                  >
                    {emoji.emoji}
                  </button>
                ))}
              </div>
            )}
          </div>
        </ScrollArea>
      </PopoverContent>
    </Popover>
  );
};

export default EmojiMenu; 